using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Tile {
    public int x;
    public int y;
    public int color;
    public int isActive;

    public Tile (int X, int Y, int color, bool isActive) {
        this.x = x;
        this.y = y;
        this.color = color;
        this.isActive = isActive;
    }
}
